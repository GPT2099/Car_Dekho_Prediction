CREATE TABLE cars (
	car_name VARCHAR(150) NOT NULL,
	car_year INT NOT NULL,
	selling_price INT,
	km_driven INT,
	fuel VARCHAR(50),
	seller_type VARCHAR(100),
	transmission VARCHAR(100),
	car_owner VARCHAR(100),
	mileage VARCHAR(100),
	engine VARCHAR(50),
	max_power varchar(100),
	torque VARCHAR (200),
	seat int

)